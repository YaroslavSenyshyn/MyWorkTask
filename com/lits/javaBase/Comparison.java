package com.lits.javaBase;

public class Comparison {

    public void result (int a, int b){

        System.out.println(a+" < "+b+" is "+(a<b));
        System.out.println(a+" > "+b+" is "+(a>b));
        System.out.println(a+" <= "+b+" is "+(a<=b));
        System.out.println(a+" >= "+b+" is "+(a>=b));
        System.out.println(a+" == "+b+" is "+(a==b));
        System.out.println(a+" != "+b+" is "+(a!=b));
    }

}

